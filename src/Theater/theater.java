package Theater;

public class theater {
	private String tTheaterNum;
	private String tCinemaName;
	private String tSeatNum;
	private String tMovieID;
	public String gettTheaterNum() {
		return tTheaterNum;
	}
	public void settTheaterNum(String tTheaterNum) {
		this.tTheaterNum = tTheaterNum;
	}
	public String gettCinemaName() {
		return tCinemaName;
	}
	public void settCinemaName(String tCinemaName) {
		this.tCinemaName = tCinemaName;
	}
	public String gettSeatNum() {
		return tSeatNum;
	}
	public void settSeatNum(String tSeatNum) {
		this.tSeatNum = tSeatNum;
	}
	public String gettMovieID() {
		return tMovieID;
	}
	public void settMovieID(String tMovieID) {
		this.tMovieID = tMovieID;
	}
}
